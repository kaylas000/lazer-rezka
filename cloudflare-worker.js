// Groq API (llama-3.3-70b-versatile) с шаблонами ответов

export default {
  async fetch(request, env) {
    const corsHeaders = {
      'Access-Control-Allow-Origin': '*',
      'Access-Control-Allow-Methods': 'POST, OPTIONS',
      'Access-Control-Allow-Headers': 'Content-Type',
    };

    if (request.method === 'OPTIONS') {
      return new Response(null, { headers: corsHeaders });
    }

    if (request.method !== 'POST') {
      return new Response('Method not allowed', { status: 405, headers: corsHeaders });
    }

    try {
      const body = await request.json();
      const { message, history, max_tokens: reqMaxTokens, system: reqSystem, mode } = body;

      if (!message) {
        return new Response(JSON.stringify({ error: true, message: 'Message is required' }), {
          status: 400,
          headers: { ...corsHeaders, 'Content-Type': 'application/json' }
        });
      }

      const systemPrompt = reqSystem || getSystemPrompt();

      const messages = [
        { role: 'system', content: systemPrompt },
        ...(history || []).slice(-10),
        { role: 'user', content: message }
      ];

      const apiResponse = await fetch('https://api.groq.com/openai/v1/chat/completions', {
        method: 'POST',
        headers: {
          'Authorization': `Bearer ${env.GROQ_API_KEY}`,
          'Content-Type': 'application/json',
        },
        body: JSON.stringify({
          model: 'llama-3.3-70b-versatile',
          messages: messages,
          max_tokens: reqMaxTokens || 800,
          temperature: mode === 'generate' ? 0.7 : 0.85
        })
      });

      if (!apiResponse.ok) {
        console.error('Groq API Error:', apiResponse.status);
        return new Response(JSON.stringify({
          error: true,
          message: 'Извините, сейчас я временно недоступен. Позвоните: +7 (985) 456-37-64 или напишите на info@lasercut.ru'
        }), {
          status: 500,
          headers: { ...corsHeaders, 'Content-Type': 'application/json' }
        });
      }

      const data = await apiResponse.json();
      const reply = data.choices[0].message.content;

      return new Response(JSON.stringify({ reply }), {
        headers: { ...corsHeaders, 'Content-Type': 'application/json' }
      });

    } catch (error) {
      console.error('Worker error:', error);
      return new Response(JSON.stringify({
        error: true,
        message: 'Произошла ошибка. Позвоните нам: +7 (985) 456-37-64'
      }), {
        status: 500,
        headers: { ...corsHeaders, 'Content-Type': 'application/json' }
      });
    }
  }
};

